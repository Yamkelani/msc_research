function dataset = generate_features(features, battery_charge_data, battery_discharge_data, columns) 

    %Add the features for each cycle. 
    charge_capacity = max(battery_charge_data.Measured_cell_capacity);
    discharge_capacity = max(abs(battery_discharge_data.Measured_cell_capacity));                  
    cycle_no = [battery_charge_data.Cycle(1)];
    charge_capacity_drop_per_cycle = [nominal_capacity - charge_capacity];
    discharge_capacity_drop_per_cycle = [nominal_capacity - abs(discharge_capacity)];
    cell_rul = [total_battery_cycles - cycle_no];
    max_charge_time = [max(battery_charge_data.Time)];
    cycle_soc = [charge_capacity/nominal_capacity];
    cycle_dod = [discharge_capacity/nominal_capacity];

    %Get constant current
    max_charge_voltage = [max(battery_charge_data.Measured_cell_voltage)];
    max_discharge_voltage = [max(battery_discharge_data.Measured_cell_voltage)];
    charge_cc_time = [max(battery_charge_data.Time(battery_charge_data.Measured_cell_voltage <= max_charge_voltage))];
    discharge_cc_time = [max(battery_discharge_data.Time(battery_discharge_data.Measured_cell_voltage <= max_discharge_voltage))];
    time_to_max_voltage = charge_cc_time;

    max_charge_current = [max(battery_charge_data.Measured_cell_current)];
    max_discharge_current = [max(abs(battery_discharge_data.Measured_cell_current))];

    %Get constant voltage time
    min_charge_voltage = [min(battery_charge_data.Measured_cell_voltage)];
    min_charge_current = [min(battery_charge_data.Measured_cell_voltage)];
    min_discharge_voltage = [min(battery_discharge_data.Measured_cell_voltage)];
    min_discharge_current = [min(battery_discharge_data.Measured_cell_voltage)];
    min_voltage_time = [battery_charge_data.Time(battery_charge_data.Measured_cell_voltage == min_charge_voltage)];
    max_voltage_time = [max(battery_charge_data.Time(battery_charge_data.Measured_cell_voltage == max_charge_voltage))];
    min_discharge_voltage_time = [battery_discharge_data.Time(battery_discharge_data.Measured_cell_voltage == min_discharge_voltage)];
    max_discharge_voltage_time = [max(battery_discharge_data.Time(battery_discharge_data.Measured_cell_voltage == max_discharge_voltage))];
    charge_cv_time = [max(min_voltage_time - max_voltage_time)];
    discharge_cv_time = [max(min_discharge_voltage_time - max_discharge_voltage_time)];
    max_charge_room_temperature = [max(battery_charge_data.Room_temperature)];
    max_discharge_room_temperature = [max(battery_discharge_data.Room_temperature)];
    min_charge_room_temperature = [min(battery_charge_data.Room_temperature)];
    min_discharge_room_temperature = [min(battery_discharge_data.Room_temperature)];
    max_charge_cell_temperature = max(battery_charge_data.Cell_temperature);
    max_discharge_cell_temperature = max(battery_discharge_data.Cell_temperature);
    min_charge_cell_temperature = min(battery_charge_data.Cell_temperature);
    min_discharge_cell_temperature = min(battery_discharge_data.Cell_temperature);
    avg_charge_room_temperature = [mean(battery_charge_data.Room_temperature)];
    avg_charge_cell_temperature = [mean(battery_charge_data.Cell_temperature)];
    avg_discharge_room_temperature = [mean(battery_discharge_data.Room_temperature)];
    avg_discharge_cell_temperature = [mean(battery_discharge_data.Cell_temperature)];
    cycle_charge_time = [max(battery_charge_data.Time)];
    cycle_discharge_time = [max(battery_discharge_data.Time)];
    time_at_max_voltage = battery_charge_data.Time(battery_charge_data.Measured_cell_voltage == max_charge_voltage);


    row = table(cycle_no, ...
        cell_rul, ... 
        cycle_charge_time, ...
        cycle_discharge_time, ...
        charge_cv_time, ...
        charge_cc_time, ... 
        discharge_cv_time, ...
        discharge_cc_time, ... 
        max_charge_voltage, ...
        min_charge_voltage, ...
        max_discharge_voltage, ...
        min_discharge_voltage, ...
        min_charge_current, ...
        max_charge_current, ...
        min_discharge_current, ...
        max_discharge_current, ...
        avg_charge_room_temperature, ...
        avg_discharge_room_temperature, ...
        avg_charge_cell_temperature, ...
        avg_discharge_cell_temperature, ...
        max_charge_cell_temperature, ... 
        max_discharge_cell_temperature, ... 
        min_charge_cell_temperature, ... 
        min_discharge_cell_temperature, ... 
        max_charge_room_temperature, ...
        max_discharge_room_temperature, ...
        min_charge_room_temperature, ...
        min_discharge_room_temperature, ...
        charge_capacity, ...
        discharge_capacity, ... 
        cycle_soc, ...
        cycle_dod, ...
        'VariableNames', columns);
    %Create the features dataset
    dataset = [features; row];
;

end
